def ReLU(Z):
    return np.maximum(Z,0)

def Softmax(Z):
    return np.exp(Z) / sum(np.exp(Z)) 

def FWDProp(W1, b1, W2, b2, X):
    Z1 = W1.dot(X) + b1
    A1 = ReLU(Z1)
    Z2 = W2.dot(A1) + b2
    A2 = Softmax(Z2)

    return Z1, A1, Z2, A2

def OneHot(Y):
    oneHotY = np.zeros((Y.size, Y.max()+1))
    oneHotY[np.arange(Y.size), Y] = 1
    oneHotY = oneHotY.T

    return oneHotY

def DerivReLU(Z):
    return Z > 0

def BWDProp(Z1, A1, Z2, A2, W2, X, Y):
    oneHotY = OneHot(Y)
    dZ2 = A2 - oneHotY
    dW2 = 1/m * dZ2.dot(A1.T)
    db2 = 1/m * np.sum(dZ2)
    dZ1 = W2.T.dot(dZ2) * DerivReLU(Z1)
    dW1 = 1/m * dZ1.dot(X.T)
    db1 = 1/m * np.sum(dZ1)

    return dW1, db1, dW2, db2

def Update(W1, b1, W2, b2, learningRate, dW1, db1, dW2, db2):
    W1 = W1 - learningRate * dW1
    b1 = b1 - learningRate * db1

    W2 = W2 - learningRate * dW2
    b2 = b2 - learningRate * db2

    return W1, b1, W2, b2

def GetPredictions(A2):
    return np.argmax(A2, 0)

def GetAccuracy(predictions, Y):
    return np.sum(predictions == Y) / Y.size


def GradientDescent(X, Y, iterations, learningRate):
    W1, b1, W2, b2 = Setup()

    for i in range(iterations):
        Z1, A1, Z2, A2 = FWDProp(W1, b1, W2, b2, X)
        dW1, db1, dW2, db2 = BWDProp(Z1, A1, Z2, A2, W2, X, Y)
        W1, b1, W2, b2 = Update(W1, b1, W2, b2, learningRate, dW1, db1, dW2, db2)

        accuracy = GetAccuracy(GetPredictions(A2), Y)
        if i % 200 == 0:
            print("Iteration: ", i)
            print("Accuracy: ", accuracy)

    return W1, b1, W2, b2

